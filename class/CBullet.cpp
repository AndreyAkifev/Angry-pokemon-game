//-------------------------------------------------------------------------//

#include "CBullet.hpp"

//-------------------------------------------------------------------------//

CBullet::CBullet( float X, float Y )
{
	x = X - 50;
	y = Y - 50;
	sfr.width = sfr.height = 32;
}

//-------------------------------------------------------------------------//

CBullet::~CBullet( )
{

}

//-------------------------------------------------------------------------//

void CBullet::Update( )
{
	x += fSpeed;
	sfr.top = y;
	sfr.left = x;
}

