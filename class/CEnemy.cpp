//-------------------------------------------------------------------------//

#include "CEnemy.hpp"
#include <iostream>

//-------------------------------------------------------------------------//

CEnemy::CEnemy( float X, float Y, float fSpd )
{
	x = X;
	y = Y;
	fSpeed = fSpd;
	sfr.width = sfr.height = 40;
}

//-------------------------------------------------------------------------//

CEnemy::~CEnemy( )
{

}

//-------------------------------------------------------------------------//

void CEnemy::Update( )
{
	x -= fSpeed;
	sfr.top = y;
	sfr.left = x;
}

